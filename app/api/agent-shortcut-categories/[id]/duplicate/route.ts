import { createClient } from "@/utils/supabase/server";
import { NextRequest, NextResponse } from "next/server";
import {
  coerceLegacyCategoryIsActive,
  platformCategoryToLegacyRow,
  PLATFORM_CATEGORY_SELECT,
} from "../_lib/categoryRow";

/**
 * POST /api/agent-shortcut-categories/[id]/duplicate
 *
 * Duplicates a shortcut category while preserving the source row's ownership
 * (`user_id`, `organization_id`, `project_id`, `task_id`). The caller may
 * override `label`, `placementType`, and `parentCategoryId`; all other fields
 * (description, iconName, color, sortOrder, isActive, enabledFeatures,
 * metadata) are copied from the source. `id`, `createdAt`, and `updatedAt` are
 * always regenerated by the database.
 */
export async function POST(
  request: NextRequest,
  context: { params: Promise<{ id: string }> },
) {
  try {
    const { id } = await context.params;
    const supabase = await createClient();
    const {
      data: { user },
      error: authError,
    } = await supabase.auth.getUser();

    if (authError || !user) {
      return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
    }

    let overrides: {
      label?: unknown;
      placement_type?: unknown;
      parent_category_id?: unknown;
      sort_order?: unknown;
    } = {};
    try {
      const raw = await request.json();
      if (raw && typeof raw === "object") {
        overrides = raw as typeof overrides;
      }
    } catch {
      // empty body is fine — fall through with defaults
    }

    const { data: sourceRow, error: sourceError } = await supabase
      .schema("platform")
      .from("categories")
      .select(PLATFORM_CATEGORY_SELECT)
      .eq("dimension", "shortcut")
      .eq("id", id)
      .maybeSingle();

    if (sourceError) {
      console.error("Error loading source category:", sourceError);
      return NextResponse.json(
        {
          error: "Failed to load source category",
          details: sourceError.message,
        },
        { status: 500 },
      );
    }

    if (!sourceRow) {
      return NextResponse.json(
        { error: "Source category not found or access denied" },
        { status: 404 },
      );
    }

    const source = platformCategoryToLegacyRow(sourceRow);

    const nextLabel =
      typeof overrides.label === "string" && overrides.label.trim().length > 0
        ? overrides.label.trim()
        : `${source.label} (Copy)`;

    const nextPlacementType =
      typeof overrides.placement_type === "string" &&
      overrides.placement_type.trim().length > 0
        ? overrides.placement_type.trim()
        : source.placement_type;

    let nextParentCategoryId: string | null = null;
    if (nextPlacementType === source.placement_type) {
      nextParentCategoryId = source.parent_category_id;
    }
    if (overrides.parent_category_id === null) {
      nextParentCategoryId = null;
    } else if (typeof overrides.parent_category_id === "string") {
      nextParentCategoryId = overrides.parent_category_id;
    }

    const nextSortOrder =
      typeof overrides.sort_order === "number"
        ? overrides.sort_order
        : source.sort_order;

    const sourceIsActive =
      source.is_active === undefined
        ? true
        : typeof source.is_active === "string"
          ? source.is_active === "true"
          : Boolean(source.is_active);

    const insertPayload = {
      dimension: "shortcut" as const,
      name: nextLabel,
      icon: source.icon_name ?? null,
      color: source.color ?? null,
      placement_type: nextPlacementType,
      parent_id: nextParentCategoryId,
      position: nextSortOrder ?? null,
      organization_id: source.organization_id ?? null,
      created_by: user.id,
      metadata: {
        ...(source.metadata as Record<string, unknown> | null ?? {}),
        description: source.description ?? null,
        is_active: sourceIsActive,
        enabled_features: source.enabled_features ?? null,
        user_id: source.user_id ?? null,
        project_id: source.project_id ?? null,
        task_id: source.task_id ?? null,
        legacy_table: "shortcut_categories",
      },
    };

    const { data, error } = await supabase
      .schema("platform")
      .from("categories")
      .insert(insertPayload as never)
      .select(PLATFORM_CATEGORY_SELECT)
      .single();

    if (error) {
      console.error("Error duplicating shortcut category:", error);
      const status =
        error.code === "42501" || error.code === "PGRST301" ? 403 : 500;
      return NextResponse.json(
        {
          error: "Failed to duplicate shortcut category",
          details: error.message,
        },
        { status },
      );
    }

    return NextResponse.json(
      {
        data: coerceLegacyCategoryIsActive(platformCategoryToLegacyRow(data)),
      },
      { status: 201 },
    );
  } catch (error) {
    console.error(
      "Error in POST /api/agent-shortcut-categories/[id]/duplicate:",
      error,
    );
    return NextResponse.json(
      {
        error: "Internal server error",
        details: error instanceof Error ? error.message : "Unknown error",
      },
      { status: 500 },
    );
  }
}
