"use client";

/**
 * NotesDemoPanel — textarea stand-in for the notes editor.
 * Target wiring (not legacy NoteContextMenu):
 *   matrx-user/notes + full surface scope + extraSections for note ops
 */

import { useCallback, useRef, useState } from "react";
import {
  buildNotesEditorContextData,
  NOTES_EDITOR_CONTEXT_MENU_PROPS,
} from "@/features/notes/agent-context/buildNotesEditorContextData";
import { buildApplicationScopeFromMenuContext } from "@/features/context-menu-v2/utils/build-application-scope";
import { createNotesEditorExtraSections } from "@/features/notes/agent-context/notesEditorExtraSections";
import { toast } from "sonner";
import { EditableContextMenu } from "@/features/context-menu-v3/EditableContextMenu";
import type { EditableContextMenuProps } from "@/features/context-menu-v3/types";
import {
  DEMO_NOTE_FOLDERS,
  DEMO_NOTE_ID,
  DEMO_NOTE_INITIAL_CONTENT,
  DEMO_NOTE_OPEN_TABS,
  DEMO_NOTE_RECORD,
  DEMO_NOTES_MAP,
} from "../_fixtures/notes-demo";
import { DemoProTextarea } from "./DemoProTextarea";

export interface NotesDemoPanelProps {
  title: string;
  description: React.ReactNode;
  initialContent?: string;
  menuOverrides?: Partial<EditableContextMenuProps>;
  minHeightClass?: string;
}

export function NotesDemoPanel({
  title,
  description,
  initialContent = DEMO_NOTE_INITIAL_CONTENT,
  menuOverrides,
  minHeightClass = "min-h-[180px]",
}: NotesDemoPanelProps) {
  const textareaRef = useRef<HTMLTextAreaElement | null>(null);
  const [content, setContent] = useState(initialContent);
  const [selectionStart, setSelectionStart] = useState(0);
  const [selectionEnd, setSelectionEnd] = useState(0);
  const [isDirty, setIsDirty] = useState(false);

  const syncSelectionFromTextarea = useCallback(() => {
    const el = textareaRef.current;
    if (!el) return;
    setSelectionStart(el.selectionStart);
    setSelectionEnd(el.selectionEnd);
  }, []);

  const contextData = buildNotesEditorContextData({
    noteId: DEMO_NOTE_ID,
    content,
    selectionStart,
    selectionEnd,
    editorMode: "plain",
    noteRecord: DEMO_NOTE_RECORD,
    isDirty,
    isAutogenerated: false,
    openTabs: DEMO_NOTE_OPEN_TABS,
    splitNoteId: null,
    allFolders: DEMO_NOTE_FOLDERS,
    notesMap: DEMO_NOTES_MAP,
  });

  const getApplicationScope = useCallback(() => {
    const el = textareaRef.current;
    const start = el?.selectionStart ?? selectionStart;
    const end = el?.selectionEnd ?? selectionEnd;
    const selectedText =
      start !== end
        ? content.slice(Math.min(start, end), Math.max(start, end))
        : "";
    return buildApplicationScopeFromMenuContext({
      selectedText,
      selectionRange: el ? { type: "editable", element: el, start, end } : null,
      contextData,
    });
  }, [content, contextData, selectionEnd, selectionStart]);

  // Demo handlers — illustrative only (this panel has no real persistence).
  const notesExtras = createNotesEditorExtraSections({
    isDirty,
    allFolders: DEMO_NOTE_FOLDERS,
    currentFolder: DEMO_NOTE_FOLDERS[0],
    openTabCount: DEMO_NOTE_OPEN_TABS.length,
    onSave: () => {
      setIsDirty(false);
      toast.success("Saved (demo)");
    },
    onDuplicate: () => toast.success("Duplicated (demo)"),
    onExport: () => toast.success("Exported (demo)"),
    onShareLink: () => toast.success("Share link (demo)"),
    onShareClipboard: () => toast.success("Copied to clipboard (demo)"),
    onMoveToFolder: (folder) => toast.success(`Moved to ${folder} (demo)`),
    onMoveDialog: () => toast.success("Move dialog (demo)"),
    onCloseTab: () => toast.success("Close tab (demo)"),
    onCloseOtherTabs: () => toast.success("Close other tabs (demo)"),
    onCloseAllTabs: () => toast.success("Close all tabs (demo)"),
    onDelete: () => toast.error("Delete note (demo)"),
  });

  const replaceContent = (next: string) => {
    setContent(next);
    setIsDirty(true);
  };

  const insertAtCursor = (text: string, position: "before" | "after") => {
    const ta = textareaRef.current;
    if (!ta) return;
    const start = ta.selectionStart;
    const end = ta.selectionEnd;
    if (position === "before") {
      replaceContent(
        content.substring(0, start) + text + "\n\n" + content.substring(start),
      );
    } else {
      replaceContent(
        content.substring(0, end) + "\n\n" + text + content.substring(end),
      );
    }
  };

  return (
    <section className="flex flex-col gap-2">
      <header>
        <h2 className="text-sm font-semibold">{title}</h2>
        <div className="text-[11px] text-muted-foreground">{description}</div>
      </header>
      <EditableContextMenu
        {...NOTES_EDITOR_CONTEXT_MENU_PROPS}
        extraSections={notesExtras}
        getTextarea={() => textareaRef.current}
        getApplicationScope={getApplicationScope}
        onTextReplace={replaceContent}
        onTextInsertBefore={(t) => insertAtCursor(t, "before")}
        onTextInsertAfter={(t) => insertAtCursor(t, "after")}
        onContentInserted={() => setIsDirty(true)}
        contextData={contextData}
        {...menuOverrides}
      >
        <DemoProTextarea
          ref={textareaRef}
          surfaceName={NOTES_EDITOR_CONTEXT_MENU_PROPS.surfaceName}
          getApplicationScope={getApplicationScope}
          value={content}
          onChange={(e) => {
            setContent(e.target.value);
            setIsDirty(true);
            setSelectionStart(e.target.selectionStart);
            setSelectionEnd(e.target.selectionEnd);
          }}
          onSelect={syncSelectionFromTextarea}
          onKeyUp={syncSelectionFromTextarea}
          onMouseUp={syncSelectionFromTextarea}
          minHeightClass={minHeightClass}
        />
      </EditableContextMenu>
    </section>
  );
}
