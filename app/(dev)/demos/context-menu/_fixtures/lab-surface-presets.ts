/**
 * Production-target contextData samples for the diagnostic lab surface picker.
 * When a manifest surface is selected, the lab loads the matching shape —
 * not a generic `{ content, context }` blob.
 */

import { buildAgentBuilderContextData } from "@/features/agents/agent-context/buildAgentBuilderContextData";
import { buildCodeWorkspaceContextData } from "@/features/code/agent-context/buildCodeWorkspaceContextData";
import { buildNotesEditorContextData } from "@/features/notes/agent-context/buildNotesEditorContextData";
import {
  DEMO_AGENT_BUILDER_SCOPE,
  DEMO_AGENT_FIELD_INITIAL,
  DEMO_AGENT_FOCUSED_FIELD,
} from "./agent-builder-demo";
import {
  DEMO_CODE_EDITOR_ALL_DIAGNOSTICS,
  DEMO_CODE_EDITOR_DIAGNOSTICS,
  DEMO_CODE_EDITOR_FILE_PATH,
  DEMO_CODE_EDITOR_INITIAL_CONTENT,
  DEMO_CODE_EDITOR_LANGUAGE,
} from "./code-editor-demo";
import {
  DEMO_NOTE_FOLDERS,
  DEMO_NOTE_ID,
  DEMO_NOTE_INITIAL_CONTENT,
  DEMO_NOTE_OPEN_TABS,
  DEMO_NOTE_RECORD,
  DEMO_NOTES_MAP,
} from "./notes-demo";
import type { SourceFeature } from "@/features/agents/types/instance.types";

export interface LabSurfacePreset {
  sourceFeature: SourceFeature;
  /** When set, applied as enabledPlacements (overrides lab placement toggles for known surfaces). */
  enabledPlacements?: string[];
  sampleContextData: Record<string, unknown>;
}

const CODE_EDITOR_SAMPLE = buildCodeWorkspaceContextData({
  fullContent: DEMO_CODE_EDITOR_INITIAL_CONTENT,
  selectedText: "",
  language: DEMO_CODE_EDITOR_LANGUAGE,
  filePath: DEMO_CODE_EDITOR_FILE_PATH,
  currentLine: 4,
  currentColumn: 1,
  lineCount: DEMO_CODE_EDITOR_INITIAL_CONTENT.split("\n").length,
  activeTabDiagnostics: DEMO_CODE_EDITOR_DIAGNOSTICS,
  allDiagnostics: DEMO_CODE_EDITOR_ALL_DIAGNOSTICS,
});

const NOTES_SAMPLE = buildNotesEditorContextData({
  noteId: DEMO_NOTE_ID,
  content: DEMO_NOTE_INITIAL_CONTENT,
  selectionStart: 0,
  selectionEnd: 0,
  editorMode: "plain",
  noteRecord: DEMO_NOTE_RECORD,
  isDirty: true,
  isAutogenerated: false,
  openTabs: DEMO_NOTE_OPEN_TABS,
  splitNoteId: null,
  allFolders: DEMO_NOTE_FOLDERS,
  notesMap: DEMO_NOTES_MAP,
});

const AGENT_BUILDER_SAMPLE = buildAgentBuilderContextData({
  agentScope: DEMO_AGENT_BUILDER_SCOPE,
  fieldContent: DEMO_AGENT_FIELD_INITIAL,
  focusedField: DEMO_AGENT_FOCUSED_FIELD,
});

const LAB_SURFACE_PRESETS: Record<string, LabSurfacePreset> = {
  "matrx-user/code-editor": {
    sourceFeature: "code-editor",
    enabledPlacements: ["ai-action", "organization-tool", "user-tool"],
    sampleContextData: CODE_EDITOR_SAMPLE,
  },
  "matrx-user/notes": {
    sourceFeature: "notes",
    enabledPlacements: ["ai-action", "content-block", "quick-action"],
    sampleContextData: NOTES_SAMPLE,
  },
  "matrx-user/agent-builder": {
    sourceFeature: "agent-builder",
    enabledPlacements: ["ai-action", "content-block", "quick-action"],
    sampleContextData: AGENT_BUILDER_SAMPLE,
  },
};

export function getLabSurfacePreset(
  surfaceName: string,
): LabSurfacePreset | null {
  return LAB_SURFACE_PRESETS[surfaceName] ?? null;
}

export function formatLabContextJson(data: Record<string, unknown>): string {
  return JSON.stringify(data, null, 2);
}
