"use client";

// FolderQuickPick — Empty-state landing grid for notes surfaces.
//
// Shown whenever a notes instance has no active note. Renders a 4-tile
// "featured" row (Draft / Scratch / Personal / Business + New Folder),
// followed by a tighter grid of every other folder the user has. Clicking
// a tile auto-creates a new note inside that folder and makes it active
// in the calling instance.
//
// Used by both the full-page NotesView (route /notes) and the floating
// NotesWindow (overlay notesBetaWindow). Don't inline this anywhere else —
// share via this single source so the empty state stays consistent.

import { useCallback, useState } from "react";
import { FileText, FolderPlus } from "lucide-react";
import { useAppDispatch, useAppSelector } from "@/lib/redux/hooks";
import {
  addInstanceTab,
  createAutogeneratedNote,
  setInstanceActiveTab,
} from "../redux/slice";
import { selectAllFolders, selectAllNotesList } from "../redux/selectors";
import { CreateFolderDialog } from "./CreateFolderDialog";
import { getFolderIconAndColor } from "../utils/folderUtils";
import { createFolder } from "../service/notesService";
import { cn } from "@/lib/utils";

const FEATURED_FOLDERS = ["Draft", "Scratch", "Personal", "Business"];

export interface FolderQuickPickProps {
  /** Notes instance the new note should be opened in. */
  instanceId: string;
  /** Optional className for the outer scroll container. */
  className?: string;
}

export function FolderQuickPick({
  instanceId,
  className,
}: FolderQuickPickProps) {
  const dispatch = useAppDispatch();
  const allFolders = useAppSelector(selectAllFolders);
  const allNotes = useAppSelector(selectAllNotesList);
  const userId = useAppSelector((s) => s.userAuth?.id);
  const [createFolderOpen, setCreateFolderOpen] = useState(false);

  const handleFolderClick = useCallback(
    (folder: string) => {
      if (!userId) return;
      const id = crypto.randomUUID();
      dispatch(createAutogeneratedNote({ id, userId, folder, instanceId }));
    },
    [dispatch, userId, instanceId],
  );

  const handleOpenNote = useCallback(
    (noteId: string) => {
      dispatch(addInstanceTab({ instanceId, noteId }));
      dispatch(setInstanceActiveTab({ instanceId, noteId }));
    },
    [dispatch, instanceId],
  );

  const recentNotes = [...allNotes]
    .sort((a, b) => (b.updated_at ?? "").localeCompare(a.updated_at ?? ""))
    .slice(0, 4);

  const handleCreateFolderConfirm = useCallback(
    async (folderName: string) => {
      try {
        await createFolder(folderName);
      } catch {
        // Folder may already exist; proceed to open a note in it
      }
      handleFolderClick(folderName);
    },
    [handleFolderClick],
  );

  const featured = FEATURED_FOLDERS.filter(
    (f) => allFolders.includes(f) || allFolders.length === 0,
  );
  const rest = allFolders.filter((f) => !FEATURED_FOLDERS.includes(f));
  const featuredToShow = allFolders.length === 0 ? FEATURED_FOLDERS : featured;

  return (
    <div
      className={cn(
        "flex-1 flex flex-col px-4 py-6 overflow-auto gap-4",
        className,
      )}
    >
      <p className="text-xs text-muted-foreground text-center shrink-0">
        Open a note from the sidebar, or start a new one:
      </p>

      {/* Featured row: larger tiles + New Folder card */}
      <div className="grid grid-cols-5 gap-2 w-full">
        {featuredToShow.map((folder) => {
          const { icon: Icon, color } = getFolderIconAndColor(folder);
          return (
            <button
              key={folder}
              className="flex flex-col items-center justify-center gap-2 py-4 rounded-xl border border-border bg-card hover:bg-accent hover:border-primary/30 transition-all cursor-pointer group"
              onClick={() => handleFolderClick(folder)}
              title={`New note in ${folder}`}
            >
              <Icon
                className={cn(
                  "w-5 h-5 shrink-0 transition-transform group-hover:scale-110",
                  color ?? "text-muted-foreground",
                )}
              />
              <span className="text-[0.7rem] font-medium text-center text-muted-foreground group-hover:text-foreground leading-tight transition-colors">
                {folder}
              </span>
            </button>
          );
        })}

        <button
          className="flex flex-col items-center justify-center gap-2 py-4 rounded-xl border border-dashed border-border bg-card/50 hover:bg-accent/50 hover:border-primary/40 transition-all cursor-pointer group"
          onClick={() => setCreateFolderOpen(true)}
          title="Create a new folder"
        >
          <FolderPlus className="w-5 h-5 shrink-0 text-muted-foreground/60 group-hover:text-primary transition-colors transition-transform group-hover:scale-110" />
          <span className="text-[0.7rem] font-medium text-center text-muted-foreground/60 group-hover:text-primary leading-tight transition-colors">
            New Folder
          </span>
        </button>
      </div>

      {/* Recent notes row */}
      {recentNotes.length > 0 && (
        <div className="flex flex-col gap-2">
          <p className="text-[0.65rem] font-medium uppercase tracking-wide text-muted-foreground/70 shrink-0">
            Recent notes
          </p>
          <div className="grid grid-cols-4 gap-2 w-full">
            {recentNotes.map((note) => {
              const folder = note.folder_name ?? "Draft";
              const { color } = getFolderIconAndColor(folder);
              const title = note.label?.trim() || "Untitled";
              return (
                <button
                  key={note.id}
                  className="flex flex-col items-start gap-1 px-3 py-3 rounded-xl border border-border bg-card hover:bg-accent hover:border-primary/30 transition-all cursor-pointer group text-left min-w-0"
                  onClick={() => handleOpenNote(note.id)}
                  title={`Open ${title}`}
                >
                  <div className="flex items-center gap-1.5 w-full min-w-0">
                    <FileText
                      className={cn(
                        "w-3.5 h-3.5 shrink-0 transition-transform group-hover:scale-110",
                        color ?? "text-muted-foreground",
                      )}
                    />
                    <span className="text-[0.7rem] font-medium text-foreground truncate flex-1 min-w-0">
                      {title}
                    </span>
                  </div>
                  <span className="text-[0.6rem] text-muted-foreground/70 truncate w-full">
                    {folder}
                  </span>
                </button>
              );
            })}
          </div>
        </div>
      )}

      {/* Remaining folders */}
      {rest.length > 0 && (
        <>
          <div className="h-px bg-border/50 shrink-0" />
          <div className="grid grid-cols-[repeat(auto-fill,minmax(80px,1fr))] gap-2 w-full">
            {rest.map((folder) => {
              const { icon: Icon, color } = getFolderIconAndColor(folder);
              return (
                <button
                  key={folder}
                  className="flex flex-col items-center justify-center gap-1.5 py-3 rounded-lg border border-border bg-card hover:bg-accent hover:border-primary/20 transition-all cursor-pointer group"
                  onClick={() => handleFolderClick(folder)}
                  title={`New note in ${folder}`}
                >
                  <Icon
                    className={cn(
                      "w-4 h-4 shrink-0 transition-transform group-hover:scale-110",
                      color ?? "text-muted-foreground",
                    )}
                  />
                  <span className="text-[0.625rem] text-center text-muted-foreground group-hover:text-foreground leading-tight line-clamp-2 transition-colors px-1">
                    {folder}
                  </span>
                </button>
              );
            })}
          </div>
        </>
      )}

      <CreateFolderDialog
        open={createFolderOpen}
        onOpenChange={setCreateFolderOpen}
        onConfirm={handleCreateFolderConfirm}
        existingFolders={allFolders}
      />
    </div>
  );
}
