-- agx_agent_bidirectional_sync.sql
--
-- Bidirectional sync between a user agent and its linked system ("builtin")
-- agent. Two agents are "linked" when one's source_agent_id references the
-- other (in either direction — convert user->system, or duplicate system->user).
--
-- This migration:
--   1. Fixes agx_duplicate_agent so a copy carries the full portable config.
--      Previously skill_config, default_rag_boost and rag_awareness_mode were
--      silently dropped to column defaults — the "duplicate loses some parts"
--      bug. The canonical portable-config column set is now identical between
--      duplicate (insert) and sync (update).
--   2. Adds agx_sync_linked_agents(from, to): copies the canonical config from
--      one agent of a linked pair to the other. Direction-agnostic — powers
--      both Pull (system -> my user copy) and Push (user -> system). Writes to
--      a builtin require super-admin; writes to a user agent require ownership.
--
-- Portable config columns (the agent "definition" that travels on copy/sync):
--   name, description, category, tags,
--   messages, variable_definitions,
--   model_id, model_tiers, settings, output_schema,
--   tools, custom_tools, context_slots, mcp_servers, tool_config,
--   skill_config, default_rag_boost, rag_awareness_mode
--
-- Deliberately NOT portable (identity / ownership / lifecycle / derived cache):
--   id, agent_type, user_id, organization_id, project_id, task_id,
--   is_active, is_public, is_archived, is_favorite,
--   source_agent_id, source_snapshot_at, version, created_at, updated_at,
--   rag_awareness_fragment, rag_awareness_refreshed_at (regenerated by backend)

-- ─── 1. agx_duplicate_agent — carry the full portable config ──────────────────

CREATE OR REPLACE FUNCTION public.agx_duplicate_agent(
  p_agent_id uuid,
  p_as_system boolean DEFAULT false
)
RETURNS uuid
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  v_source     record;
  v_new_id     uuid;
  v_uid        uuid    := auth.uid();
  v_as_system  boolean := COALESCE(p_as_system, false);
BEGIN
  IF v_uid IS NULL THEN
    RAISE EXCEPTION 'Not authenticated';
  END IF;

  SELECT * INTO v_source FROM agx_agent WHERE id = p_agent_id;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Agent not found';
  END IF;

  IF NOT check_resource_access(
    'agx_agent', p_agent_id, 'viewer',
    v_source.user_id, NULL, v_source.project_id, v_source.organization_id
  ) AND v_source.is_public = false THEN
    RAISE EXCEPTION 'Access denied';
  END IF;

  -- Promoting to a system agent is super-admin-only. Deliberately strict so
  -- a forged client param can never produce a builtin from a non-admin call.
  IF v_as_system AND NOT is_super_admin() THEN
    RAISE EXCEPTION 'Only super admins can duplicate as a system agent';
  END IF;

  v_new_id := gen_random_uuid();

  IF v_as_system THEN
    INSERT INTO agx_agent (
      id, agent_type, name, description,
      messages, variable_definitions, model_id, model_tiers, settings, output_schema,
      tools, custom_tools, context_slots, mcp_servers, tool_config,
      skill_config, default_rag_boost, rag_awareness_mode,
      category, tags,
      is_active, is_public, is_archived, is_favorite,
      user_id, organization_id, project_id, task_id,
      source_agent_id, source_snapshot_at
    )
    VALUES (
      v_new_id, 'builtin', v_source.name || ' (Copy)', v_source.description,
      v_source.messages, v_source.variable_definitions, v_source.model_id,
        v_source.model_tiers, v_source.settings, v_source.output_schema,
      v_source.tools, v_source.custom_tools, v_source.context_slots,
        v_source.mcp_servers, v_source.tool_config,
      v_source.skill_config, v_source.default_rag_boost, v_source.rag_awareness_mode,
      v_source.category, v_source.tags,
      true,           -- is_active
      true,           -- is_public — system agents are visible to everyone
      false,          -- is_archived
      false,          -- is_favorite
      NULL,           -- user_id — system agents have no owner
      NULL,           -- organization_id
      NULL,           -- project_id
      NULL,           -- task_id
      p_agent_id,     -- source_agent_id — track lineage
      now()           -- source_snapshot_at
    );
  ELSE
    INSERT INTO agx_agent (
      id, agent_type, name, description,
      messages, variable_definitions, model_id, model_tiers, settings, output_schema,
      tools, custom_tools, context_slots, mcp_servers, tool_config,
      skill_config, default_rag_boost, rag_awareness_mode,
      category, tags,
      is_active, is_public, is_archived, is_favorite,
      user_id, organization_id, project_id, task_id,
      source_agent_id, source_snapshot_at
    )
    VALUES (
      v_new_id, 'user', v_source.name || ' (Copy)', v_source.description,
      v_source.messages, v_source.variable_definitions, v_source.model_id,
        v_source.model_tiers, v_source.settings, v_source.output_schema,
      v_source.tools, v_source.custom_tools, v_source.context_slots,
        v_source.mcp_servers, v_source.tool_config,
      v_source.skill_config, v_source.default_rag_boost, v_source.rag_awareness_mode,
      v_source.category, v_source.tags,
      true, false, false, false,
      v_uid, NULL, NULL, NULL,
      p_agent_id, now()
    );
  END IF;

  RETURN v_new_id;
END;
$function$;

-- ─── 2. agx_sync_linked_agents — copy config across a linked pair ─────────────

-- p_include_identity: when true (push / publish), the target also takes the
-- source's name/description/category/tags. When false (pull into a personal
-- copy), those identity fields are preserved so a refresh never renames the
-- user's own copy — only behavior syncs.
CREATE OR REPLACE FUNCTION public.agx_sync_linked_agents(
  p_from_id          uuid,
  p_to_id            uuid,
  p_include_identity boolean DEFAULT true
)
RETURNS uuid
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  v_from        record;
  v_to          record;
  v_uid         uuid := auth.uid();
  v_derived_id  uuid;
  v_identity    boolean := COALESCE(p_include_identity, true);
BEGIN
  IF v_uid IS NULL THEN
    RAISE EXCEPTION 'Not authenticated';
  END IF;

  IF p_from_id = p_to_id THEN
    RAISE EXCEPTION 'Cannot sync an agent with itself';
  END IF;

  SELECT * INTO v_from FROM agx_agent WHERE id = p_from_id;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Source agent not found';
  END IF;

  SELECT * INTO v_to FROM agx_agent WHERE id = p_to_id;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Target agent not found';
  END IF;

  -- The two agents must form a linked pair: one was derived from the other.
  -- We record which side carries the lineage pointer so we can stamp its
  -- source_snapshot_at as the "pair last reconciled at" marker below.
  IF v_to.source_agent_id = v_from.id THEN
    v_derived_id := v_to.id;
  ELSIF v_from.source_agent_id = v_to.id THEN
    v_derived_id := v_from.id;
  ELSE
    RAISE EXCEPTION 'Agents are not linked (no shared lineage)';
  END IF;

  -- Caller must be able to read the source side.
  IF NOT (v_from.agent_type = 'builtin' AND v_from.is_active) THEN
    IF NOT check_resource_access(
      'agx_agent', v_from.id, 'viewer',
      v_from.user_id, NULL, v_from.project_id, v_from.organization_id
    ) AND v_from.is_public = false THEN
      RAISE EXCEPTION 'Access denied to source agent';
    END IF;
  END IF;

  -- Write gating on the target.
  --   builtin  -> publishing to the shared catalogue, super-admin only
  --   user/org -> must own it (or be super-admin)
  IF v_to.agent_type = 'builtin' THEN
    IF NOT is_super_admin() THEN
      RAISE EXCEPTION 'Only super admins can sync into a system agent';
    END IF;
  ELSE
    IF v_to.user_id IS DISTINCT FROM v_uid AND NOT is_super_admin() THEN
      RAISE EXCEPTION 'You can only sync into an agent you own';
    END IF;
  END IF;

  -- Copy the canonical portable config from -> to. Identity, ownership,
  -- lifecycle flags and derived caches are intentionally left untouched.
  UPDATE agx_agent SET
    name                 = CASE WHEN v_identity THEN v_from.name        ELSE name        END,
    description          = CASE WHEN v_identity THEN v_from.description ELSE description END,
    category             = CASE WHEN v_identity THEN v_from.category    ELSE category    END,
    tags                 = CASE WHEN v_identity THEN v_from.tags        ELSE tags        END,
    messages             = v_from.messages,
    variable_definitions = v_from.variable_definitions,
    model_id             = v_from.model_id,
    model_tiers          = v_from.model_tiers,
    settings             = v_from.settings,
    output_schema        = v_from.output_schema,
    tools                = v_from.tools,
    custom_tools         = v_from.custom_tools,
    context_slots        = v_from.context_slots,
    mcp_servers          = v_from.mcp_servers,
    tool_config          = v_from.tool_config,
    skill_config         = v_from.skill_config,
    default_rag_boost    = v_from.default_rag_boost,
    rag_awareness_mode   = v_from.rag_awareness_mode,
    updated_at           = now()
  WHERE id = v_to.id;

  -- Mark the pair as reconciled now.
  UPDATE agx_agent SET source_snapshot_at = now() WHERE id = v_derived_id;

  RETURN v_to.id;
END;
$function$;

GRANT EXECUTE ON FUNCTION public.agx_sync_linked_agents(uuid, uuid, boolean) TO authenticated;

-- ─── 3. agx_update_from_source — delegate to the sync engine ──────────────────
--
-- Pre-existing one-arg "pull from my source" entry point. It previously copied
-- only a subset of config columns AND had no authorization check (any caller
-- could overwrite any sourced agent). Reimplemented as a thin, identity-
-- preserving wrapper over agx_sync_linked_agents so there is a single sync
-- engine with a single, audited gate. Signature/return shape unchanged, so the
-- existing `updateAgentFromSource` thunk keeps working.

CREATE OR REPLACE FUNCTION public.agx_update_from_source(p_agent_id uuid)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  v_target record;
  v_source record;
BEGIN
  SELECT * INTO v_target FROM agx_agent WHERE id = p_agent_id;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('success', false, 'error', 'Agent not found');
  END IF;
  IF v_target.source_agent_id IS NULL THEN
    RETURN jsonb_build_object('success', false, 'error', 'Agent has no source');
  END IF;
  SELECT * INTO v_source FROM agx_agent WHERE id = v_target.source_agent_id;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('success', false, 'error', 'Source agent not found');
  END IF;

  PERFORM set_config(
    'app.change_note',
    'Updated from source: ' || v_source.name || ' (v' || v_source.version || ')',
    true
  );

  -- Pull: identity preserved, full behavior config, properly gated.
  PERFORM agx_sync_linked_agents(v_source.id, v_target.id, false);

  RETURN jsonb_build_object(
    'success', true,
    'source_version', v_source.version,
    'agent_name', v_target.name
  );
END;
$function$;
