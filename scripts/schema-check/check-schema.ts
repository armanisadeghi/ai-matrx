#!/usr/bin/env tsx
/**
 * check-schema — the schema-truth-check orchestrator.
 *
 * Pulls the LIVE database (one cached snapshot) and diffs it against everything
 * the frontend code assumes about the schema: the generated DB types, the Python
 * API types, every direct `.from()/.schema()` call, raw `schema.table` strings,
 * typed `Database[...]` refs, and the dead-relations registry. The 2026 clean-cut
 * doctrine means a moved/retired table has no shim — stale refs ERROR at runtime
 * with nothing at build time to catch them. This makes the terminal light up RED
 * first. See ./FEATURE.md.
 *
 *   pnpm check:schema             # loud tiered report, exit 0 (non-blocking)
 *   pnpm check:schema:strict      # exit 1 on any error (CI gate)
 *   pnpm check:schema:refresh     # re-pull the live snapshot first, then check
 *   tsx … --warn                  # include warnings
 *   tsx … --verbose               # full per-finding dump (default summarizes big lists)
 *   tsx … --only types-freshness,direct-from-schema
 *
 * Adding a check is one file in checks/ — see ./FEATURE.md.
 */
import { execFileSync } from "node:child_process";
import { dirname, resolve } from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";
import { buildContext, registeredChecks } from "./context";
import { classifyGenerated } from "./generated-files";
import "./checks"; // registers every check
import type { Finding, GeneratedInfo } from "./types";

const C = {
  reset: "\x1b[0m",
  bold: "\x1b[1m",
  dim: "\x1b[2m",
  red: "\x1b[31m",
  green: "\x1b[32m",
  yellow: "\x1b[33m",
  cyan: "\x1b[36m",
};

// Tiering: which checks belong to which loud band (loudest first).
const TIER_SCHEMA = new Set(["schema-exposure"]);
const TIER_GENERATED = new Set(["types-freshness", "api-types-freshness"]);
// everything else = source references

export interface RunOptions {
  only?: string[];
  argv?: string[];
}

function box(lines: string[], color: string): void {
  const width = Math.max(...lines.map((l) => l.length));
  const bar = "═".repeat(width + 2);
  console.error(`${color}${C.bold}╔${bar}╗${C.reset}`);
  for (const l of lines) console.error(`${color}${C.bold}║ ${l.padEnd(width)} ║${C.reset}`);
  console.error(`${color}${C.bold}╚${bar}╝${C.reset}`);
}

function topFiles(items: Finding[], n = 8): Array<[string, number]> {
  const counts = new Map<string, number>();
  for (const f of items) {
    const key = (f.location ?? "?").replace(/:\d+$/, "");
    counts.set(key, (counts.get(key) ?? 0) + 1);
  }
  return [...counts].sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0])).slice(0, n);
}

/** Loud, unmissable: editing a generated file will NOT fix the drift. */
function printGeneratedSection(findings: Finding[], verbose: boolean): void {
  const byArtifact = new Map<string, { info: GeneratedInfo; items: Finding[] }>();
  for (const f of findings) {
    const info = f.generated!;
    const g = byArtifact.get(info.label) ?? { info, items: [] };
    g.items.push(f);
    byArtifact.set(info.label, g);
  }
  for (const { info, items } of byArtifact.values()) {
    console.error("");
    box(
      [
        `AUTOGENERATED FILE — editing it will NOT fix this. Regenerate the source.`,
        `${info.label}`,
      ],
      C.yellow,
    );
    console.error(`  ${C.bold}Source of truth:${C.reset} ${info.source}`);
    console.error(`  ${C.bold}${C.green}Regenerate with:${C.reset} ${C.green}${info.regen}${C.reset}`);
    console.error(`  ${C.dim}Why a hand-edit won't survive: ${info.why}${C.reset}`);
    const errors = items.filter((i) => i.severity === "error");
    const warns = items.filter((i) => i.severity === "warn");
    const show = verbose ? items : [...errors.slice(0, 25), ...warns.slice(0, 10)];
    for (const f of show) {
      const color = f.severity === "error" ? C.red : C.yellow;
      console.error(`  ${color}${f.severity.toUpperCase()}${C.reset} ${f.message}`);
    }
    const hidden = items.length - show.length;
    if (hidden > 0) console.error(`  ${C.dim}…and ${hidden} more (use --verbose)${C.reset}`);
  }
}

function printRefSection(findings: Finding[], verbose: boolean): void {
  const byCheck = new Map<string, Finding[]>();
  for (const f of findings) (byCheck.get(f.check) ?? byCheck.set(f.check, []).get(f.check)!).push(f);
  for (const name of [...byCheck.keys()].sort()) {
    const items = byCheck.get(name)!;
    const nErr = items.filter((i) => i.severity === "error").length;
    const nWarn = items.length - nErr;
    console.error(`\n${C.bold}● ${name}${C.reset}  ${C.dim}(${nErr} error, ${nWarn} warn)${C.reset}`);
    if (verbose || items.length <= 12) {
      for (const f of items) {
        const color = f.severity === "error" ? C.red : C.yellow;
        console.error(`  ${color}${f.location ?? ""}${C.reset}  ${f.message}`);
        if (f.fix) console.error(`      ${C.dim}↳ ${f.fix}${C.reset}`);
      }
    } else {
      for (const [file, cnt] of topFiles(items))
        console.error(`  ${C.yellow}${String(cnt).padStart(4)}${C.reset}  ${C.dim}${file}${C.reset}`);
      console.error(`  ${C.dim}(${items.length} total — use --verbose for every line)${C.reset}`);
    }
  }
}

export function main(opts: RunOptions = {}): number {
  const argv = opts.argv ?? process.argv.slice(2);
  const strict = argv.includes("--strict");
  const warn = argv.includes("--warn");
  const verbose = argv.includes("--verbose");
  const refresh = argv.includes("--refresh");
  const onlyArg = argv.find((a) => a.startsWith("--only="))?.slice(7) ?? argv[argv.indexOf("--only") + 1];
  const only = new Set(
    opts.only ?? (argv.includes("--only") || argv.some((a) => a.startsWith("--only=")) ? (onlyArg ?? "").split(",").map((s) => s.trim()).filter(Boolean) : []),
  );

  const root = resolve(dirname(fileURLToPath(import.meta.url)), "..", "..");

  if (refresh) {
    try {
      execFileSync("npx", ["tsx", resolve(root, "scripts/schema-check/get-current-schema.ts")], { stdio: "inherit" });
    } catch {
      console.error(`${C.yellow}[WARN]${C.reset} snapshot refresh failed — using the committed snapshot.`);
    }
  }

  const ctx = buildContext(root, warn);
  const snap = ctx.snapshot;

  const all: Finding[] = [];
  const ran: string[] = [];
  for (const { name, fn } of registeredChecks()) {
    if (only.size && !only.has(name)) continue;
    ran.push(name);
    all.push(...fn(ctx));
  }

  // Auto-fill the generated flag for any finding sitting in a generated file.
  for (const f of all) {
    if (!f.generated && f.location) {
      const info = classifyGenerated(f.location.replace(/:\d+$/, ""));
      if (info) f.generated = info;
    }
  }

  const errors = all.filter((f) => f.severity === "error");
  const warns = all.filter((f) => f.severity === "warn");
  const shown = warn ? all : errors;

  const nTables = [...snap.tables.values()].reduce((a, s) => a + s.size, 0);
  const provenance = `${snap.source} · ${snap.tables.size} schemas / ${nTables} tables`;

  if (shown.length === 0) {
    const hiddenWarns = !warn && warns.length ? ` ${C.dim}(${warns.length} warning(s) hidden — use --warn)${C.reset}` : "";
    console.log(
      `${C.green}${C.bold}✓ schema-truth-check: code matches the live DB${C.reset} ${C.dim}across ${ran.length} check(s) — ${provenance}.${C.reset}${hiddenWarns}`,
    );
    if (snap.provenance === "db-types" || snap.provenance === "none")
      console.log(`${C.yellow}  note:${C.reset} ${C.dim}ran against a DEGRADED snapshot (${snap.source}). Run \`pnpm check:schema:refresh\` for full coverage.${C.reset}`);
    return strict && errors.length ? 1 : 0;
  }

  console.error("");
  box(["SCHEMA TRUTH-CHECK — code no longer matches the LIVE database"], C.red);
  if (snap.provenance === "db-types" || snap.provenance === "none")
    console.error(`${C.yellow}  snapshot is DEGRADED (${snap.source}) — freshness/exposure checks are limited. Run \`pnpm check:schema:refresh\`.${C.reset}`);

  const schemaTier = shown.filter((f) => TIER_SCHEMA.has(f.check));
  const generatedTier = shown.filter((f) => f.generated || TIER_GENERATED.has(f.check));
  const refTier = shown.filter((f) => !TIER_SCHEMA.has(f.check) && !f.generated && !TIER_GENERATED.has(f.check));

  if (schemaTier.length) {
    console.error("");
    box([`MISSING / UNEXPOSED SCHEMAS — ${schemaTier.length}`], C.red);
    for (const f of schemaTier) {
      console.error(`  ${C.red}${f.location ?? ""}${C.reset}  ${f.message}`);
      if (f.fix) console.error(`      ${C.dim}↳ ${f.fix}${C.reset}`);
    }
  }

  if (generatedTier.length) printGeneratedSection(generatedTier, verbose);

  if (refTier.length) {
    console.error(`\n${C.red}${C.bold}━━ DEAD / WRONG REFERENCES IN SOURCE (${refTier.length}) ━━${C.reset}`);
    printRefSection(refTier, verbose);
  }

  console.error(
    `\n${C.red}${C.bold}${errors.length} error(s)${C.reset}, ${C.yellow}${warns.length} warn(s)${C.reset} ` +
      `across ${new Set(shown.map((f) => f.location).filter(Boolean)).size} location(s). ${C.dim}(${provenance})${C.reset}`,
  );
  if (!warn && warns.length) console.error(`${C.dim}+${warns.length} warning(s) hidden — use --warn.${C.reset}`);
  console.error("");

  return strict && errors.length ? 1 : 0;
}

// Run when invoked directly (not when imported by the dead-relations entry).
if (process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  process.exit(main());
}
